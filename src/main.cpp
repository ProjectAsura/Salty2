#include <renderer.h>


//-----------------------------------------------------------------------------
//      メインエントリーポイントです.
//-----------------------------------------------------------------------------
int main(int argc, char** argv)
{
    Renderer::Desc desc = {};
    desc.Width   = 3840;
    desc.Height  = 2160;
    desc.Seconds = 0;

    Renderer renderer;
    if (renderer.Init(desc))
    { renderer.Run(); }
    renderer.Term();

    return 0;
}